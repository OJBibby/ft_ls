#include "libft.h"

int	ft_isalnum(int x)
{
	if (x <= 'Z' && x >= 'A')
		return (1);
	if (x <= 'z' && x >= 'a')
		return (2);
	if (x <= '9' && x >= '0')
		return (4);
	return (0);
}
