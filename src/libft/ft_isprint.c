#include "libft.h"

int	ft_isprint(int x)
{
	if (x == 32)
		return (64);
	if ((x >= 33 && x <= 47) || (x >= 58 && x <= 64))
		return (16);
	if ((x >= 91 && x <= 96) || (x >= 123 && x <= 126))
		return (16);
	if (x >= 48 && x <= 57)
		return (4);
	if (x >= 65 && x <= 90)
		return (1);
	if (x >= 97 && x <= 122)
		return (2);
	return (0);
}
